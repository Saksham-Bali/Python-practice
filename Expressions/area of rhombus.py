a = float(input("Enter the value of the first parallel side of the rhombus: "))
b = float(input("Enter the value of the second parallel side of the rhombus: "))
h = float(input("Enter the value of the perpendicular length between the parallel sides of the rhombus: "))
area = 0.5 * (a + b) * h
print("The area of the rhombus is: ", area)
